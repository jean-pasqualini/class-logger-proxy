<?php

namespace App\Entity;

abstract class AbstractEntityObject implements EntityObjectInterface
{
    private $id;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
}